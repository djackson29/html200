function squareNumber() {
  let input = prompt('Enter any number and I will square it for you!');
  let result = 0;

  result = Number(input) * Number(input);

  alert(result);
}

function multiplier() {
  let input = prompt('Enter first number');
  let inputTwo = prompt('Enter second number');
  let result = 0;

  result = Number(input) * Number(inputTwo);

  alert(result);
}
